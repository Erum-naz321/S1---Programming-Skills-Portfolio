// Exercise 2: Simple Sums
// This program adds two integers and displays the total


#include <iostream>
using namespace std;

int main() {
    // Declare integer variables
    int a = 8;   // First number
    int b = 10;  // Second number

    // Add them together
    int sum = a + b;

    // Print the result
    cout << sum;

    return 0;
}




